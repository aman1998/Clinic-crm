import { abbreviateNumber } from './abbreviateNumber';

describe.each`
	num              | expected
	${-1}            | ${'-1'}
	${1}             | ${'1'}
	${10}            | ${'10'}
	${100}           | ${'0.1K'}
	${1000}          | ${'1K'}
	${1500}          | ${'1.5K'}
	${1525}          | ${'1.5K'}
	${-1525}         | ${'-1.5K'}
	${-1525.55}      | ${'-1.5K'}
	${10000}         | ${'10K'}
	${100000}        | ${'0.1M'}
	${1200000}       | ${'1.2M'}
	${12000000}      | ${'12M'}
	${12000000.457}  | ${'12M'}
	${987654321}     | ${'1.0B'}
	${'987654321'}   | ${'1.0B'}
	${9876543210}    | ${'9.9B'}
	${98765432100}   | ${'99B'}
	${987654321000}  | ${'1.0T'}
	${-987654321000} | ${'-1.0T'}
`('abbreviateNumber($num)', ({ num, expected }) => {
	test(`returns ${expected}`, () => {
		expect(abbreviateNumber(num)).toEqual(expected);
	});
});
